let n = 10;

// pos-incremento
console.log("n++: " + n++ + " / " + n);

// pre-decremento
console.log("++n: " + n-- + " / " + n);

// pos-incremento
console.log("n--: " + ++n + " / " + n);

// pre-decremento
console.log("--n: " + --n + " / " + n);

// atribuição composta positiva
n += 10;

console.log("n += 10: " + n + " / " + n);

// atribuição composta negativa
n -= 10;

console.log("n -= 10: " + n + " / " + n);
